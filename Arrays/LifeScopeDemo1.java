class LifeScopeDemo1
{
	public static void main(String args[])
	{
		int  a;
		a=1;
		System.out.println("value of a is"+a);
		{
			int b;
			b=2;
			
		System.out.println("value of a in first"+"  "+a);
		System.out.println("value of b in first"+"  "+b);
	{
		int c=3;
	System.out.println("value of a in first"+"  "+a);
		System.out.println("value of b in first"+"  "+b);
		System.out.println("value of c in first"+"  "+c);
	}
}	
	}
			  
	}
